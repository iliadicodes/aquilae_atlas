// ── Run ───────────────────────────────────────────────────────────────────────

async function seed() {
  console.log('Seeding legions...');
  const { error: le } = await supabase.from('legions').upsert(legions);
  if (le) { console.error('legions:', le.message); process.exit(1); }
  console.log(`  ✓ ${legions.length} legions`);

  console.log('Seeding movement_stages...');
  const { error: se } = await supabase.from('movement_stages').upsert(stages);
  if (se) { console.error('movement_stages:', se.message); process.exit(1); }
  console.log(`  ✓ ${stages.length} stages`);

  console.log('Seeding pois...');
  const { error: pe } = await supabase.from('pois').upsert(pois);
  if (pe) { console.error('pois:', pe.message); process.exit(1); }
  console.log(`  ✓ ${pois.length} POIs`);

  console.log('Done.');
}

seed();
